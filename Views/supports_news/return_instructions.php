            <h1>HƯỚNG DẪN ĐỔI TRẢ ---</h1>
            <div class="strong">
                <strong>1. Kiểm tra điều kiện đổi trả hàng:</strong>
            </div>
                <p>- Quý khách vui lòng chắc chắn rằng sản phẩm yêu cầu đổi (trả) thỏa mãn đầy đủ các điều kiện đổi trả hàng qui định tại <a href="?act=return_policy"> Chính sách đổi trả.</a></p>
                <p>- Một số điều kiện bắt buộc khác:</p>
                <p>&nbsp;&nbsp;♦ Sản phẩm không thuộc danh mục hạn chế đổi trả hoặc không hỗ trợ đổi trả,</p>
                <p>&nbsp;&nbsp;♦ Gửi yêu cầu đổi (trả) không quá 3 ngày, kể từ ngày nhận hàng,</p>
                <p>&nbsp;&nbsp;♦ Sản phẩm còn nguyên bao bì đóng gói, đầy đủ phụ kiện, tem nhãn, còn phiếu bảo hành cùng quà tặng đi kèm (nếu có) và không có dấu hiệu đã qua sử dụng.</p>
            <div class="strong">
                <strong>2. Các bước thực hiện đổi trả</strong>
            </div>
            <div class="strong">
                <strong>Bước 1: Đăng ký đổi trả</strong>
            </div>
                <p>Khách hàng liên hệ với bộ phận CSKH của shopsport qua hotline 1800 6725 hoặc truy cập <b>website: </b><a href="">www.shopsport.vn</a> và gửi yêu cầu đổi (trả) sản phẩm tại phần Liên hệ hỗ trợ, đồng thời cung cấp thông tin chi tiết về sản phẩm lỗi bao gồm:</p>
                <p>- Mã số đơn hàng</p>
                <p>- Tên sản phẩm</p>
                <p>- Lỗi của sản phẩm (mô tả chi tiết)</p>
                <p>- Nhu cầu cần hỗ trợ: Đổi sản phẩm mới, Hoàn tiền hay Bảo hành</p>
            <div class="strong">
                <strong>Bước 2: Nhận email/tin nhắn xác nhận đã đăng ký đổi (trả) thành công</strong>
            </div>
                <p>Sau khi yêu cầu đổi (trả) của Quý khách được chấp nhận, shopsport.vn sẽ gửi đến Quý khách hàng email hướng dẫn hoặc một tin nhắn SMS có kèm mã gửi hàng miễn phí về shopsport.vn thông qua các đối tác giao hàng.</p>
            <div class="strong">
                <strong>Bước 3: Đóng gói và mang sản phẩm đến Bưu điện</strong>
            </div>
                <p>Quý khách thực hiện đóng gói và mang sản phẩm cùng các giấy tờ đi kèm của sản phẩm muốn đổi (trả) đến Bưu điện gần nhất để gửi hàng cho chúng tôi.</p>
                <i>Tham khảo mạng lưới bưu điện trên toàn quốc theo đường link sau: </i><span><b>ViettelPost: </b><a href="">http://www.viettelpost.com.vn/PostOfficeNetwork</a></sp>
            <div class="strong">
                <strong>Bước 4: Dán phiếu đổi trả</strong>
            </div>
                <p>- Quý khách vui lòng dán phiếu đổi trả và  điền đầy đủ các thông tin dán bên ngoài kiện hàng, bao gồm:</p>
                <p>&nbsp;&nbsp;♦ Mã đơn hàng,</p>
                <p>&nbsp;&nbsp;♦ Mã gửi hàng miễn phí,</p>
                <p>&nbsp;&nbsp;♦ Yêu cầu Đổi hay Trả sản phẩm,</p>
                <p>&nbsp;&nbsp;♦ Lý do đổi/trả,</p>
                <p>&nbsp;&nbsp;♦ Phương thức hoàn tiền (nếu có).</p>
                <p>- Quý khách vui lòng gửi sản phẩm qua đường Bưu điện về shopsport.vn trong vòng 03 ngày làm việc, kể từ khi thông báo với Bộ phận CSKH, đồng thời gửi kèm các giấy tờ sau:</p>
                <p>&nbsp;&nbsp;♦ Hóa đơn bán hàng hoặc Đơn đặt hàng có ghi chú mã đơn hàng</p>
                <p>&nbsp;&nbsp;♦ Hóa đơn giá trị gia tăng (nếu có)</p>
                <p>&nbsp;&nbsp;♦ Phụ kiện đi kèm sản phẩm và quà tặng khuyến mãi (nếu có)</p>
            <div class="strong">
                <strong>Bước 5: Gửi sản phẩm về shopsport.vn</strong>
            </div>
                <p>Khách hàn gửi sản phẩm đổi trả đã đóng gói về cho shopsport.vn theo địa chỉ:</p>
                <p>- Công ty Cổ phần Công nghệ thông tin Thạch Thanh(Hà Tĩnh)</p>
                <p>- Cây xăng Thạch Thanh, Thạch Hà, Hà Tĩnh</p>
                <i><b>Lưu ý:</b></i>
                <p>- Quý khách vui lòng chỉ gửi sản phẩm qua đường bưu điện và chịu trách nhiệm về tình trạng nguyên vẹn của sản phẩm khi gửi về shopsport.vn. Quý khách có thể ghi nhớ lại mã bưu vận để thuận tiện khi tra cứu thông tin kiện hàng gửi đổi trả tại: <b>ViettelPost:</b><a href="http://www.viettelpost.com.vn/">http://www.viettelpost.com.vn/</a></p>
                <p>- Sau khi nhận được sản phẩm gửi đến, shopsport.vn sẽ liên hệ với khách hàng và cập nhật thông tin từng giai đoạn xử lý hàng đổi trả đến Quý khách.</p>
                <p>- Quy định này có hiệu lực từ ngày 30/12/2021.</p>
